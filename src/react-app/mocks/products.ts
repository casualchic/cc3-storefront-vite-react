// Helper functions
export const getProductsByCategory = (categorySlug: string, subcategorySlug?: string) => {
  return products.filter((product) => {
    if (product.category !== categorySlug) return false;
    if (subcategorySlug && product.subcategory !== subcategorySlug) return false;
    return true;
  });
};

export const getSaleProducts = () => {
  return products.filter((product) => product.isSale);
};

export const getNewProducts = () => {
  return products.filter((product) => product.isNew);
};

export const getProductById = (id: string) => {
  return products.find((product) => product.id === id);
};
